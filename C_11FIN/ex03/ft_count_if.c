int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	i;
	int	zero;

	i = 0;
	zero = 0;
	while (i < length)
	{
		if (f(tab[i]) != 0)
			zero++;
		i++;
	}
	return (zero);
}
