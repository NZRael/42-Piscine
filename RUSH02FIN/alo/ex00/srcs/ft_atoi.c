#include "rush02.h"

unsigned int	ft_atoi(char *str)
{
	int				c;
	unsigned int	atoi;

	c = 0;
	atoi = 0;
	while (str[c])
	{
		if (str[c] >= '0' && str[c] <= '9')
		{
			atoi = (atoi * 10) + (str[c] - 48);
		}
		c++;
	}
	return (atoi);
}
