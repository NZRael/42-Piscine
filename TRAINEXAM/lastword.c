#include <unistd.h>

int main(int argc, char **argv)
{
	int		i;
	int		j;
	char	*str;

	str = argv[1];
	i = 0;
	j = 0;
	if (argc == 2)
	{
		while (str[i] != '\0')
		{
			if ((str[i] == ' ' || str[i] == '\t') && (str[i+1] >= 33 && str[i+1] <= 127))
				j = i + 1;
			i++;
		}
		while (str[j] >= 33 && str[j] <= 127)
		{
			write(1, &str[j], 1);
			j++;
		}
	}
	write(1, "\n", 1);
	return (0);
}